#!/bin/sh

FILELIST_NAME=anything-filelist.all.filelist
FILELIST_NAME_TMP=${FILELIST_NAME}.tmp
FILELIST_ORIGDIR=$HOME/.emacs.d/var/
FILELIST_RAMDIR=/tmp

MAKE_FILELIST=$HOME/.emacs.d/bin/make-filelist.rb

sudo ruby ${MAKE_FILELIST} > ${FILELIST_RAMDIR}/${FILELIST_NAME_TMP}

cp ${FILELIST_RAMDIR}/${FILELIST_NAME_TMP} \
   ${FILELIST_ORIGDIR}/${FILELIST_NAME}

mv ${FILELIST_RAMDIR}/${FILELIST_NAME_TMP} \
   ${FILELIST_RAMDIR}/${FILELIST_NAME}
